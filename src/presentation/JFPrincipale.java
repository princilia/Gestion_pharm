    private void jMenuItemDeconnexionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemDeconnexionActionPerformed
           
        int rep = JOptionPane.showConfirmDialog(null,"Voulez-vous vous deconnectez  ?","Gestion Pharm",JOptionPane.YES_NO_OPTION);
        
        if (rep == JOptionPane.YES_OPTION){
          jDesktopPane1.removeAll();
          JIFLogin jiflogin = new JIFLogin(this);
          jDesktopPane1.add(jiflogin);
          jMenuFichier.setEnabled(false);
          jMenuListeChoix.setEnabled(false);
          
          try {
              jiflogin.setMaximum(true);
              jiflogin.setVisible(true);
          } catch (PropertyVetoException ex) {
              Logger.getLogger(JFPrincipale.class.getName()).log(Level.SEVERE, null, ex);
          }
          this.setTitle(titre);
          jDesktopPane1.repaint();
         
      }
    }//GEN-LAST:event_jMenuItemDeconnexionActionPerformed

