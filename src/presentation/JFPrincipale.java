    public void t(Utilisateur ut){
        this.utilisateur = ut;
        this.setTitle(titre + " - " + ut.getNomId());
        jDesktopPane1.removeAll();
        JIFVente g = new JIFVente(ut);
        jDesktopPane1.add(g);
        try {
            g.setVisible(true);
            g.setMaximum(true);
        } catch (PropertyVetoException ex) {
            Logger.getLogger(JFPrincipale.class.getName()).log(Level.SEVERE, null, ex);
        }
        jDesktopPane1.repaint();
    }
    
